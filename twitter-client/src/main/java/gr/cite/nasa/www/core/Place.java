package gr.cite.nasa.www.core;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

@XmlAccessorType(XmlAccessType.NONE)
public class Place {
		
	@XmlElement(name="woeid")
	private String woeid;
	
	@XmlElement(name="placeTypeName")
	private PlaceInfo placeTypeName;
	
	@XmlElement(name="name")
	private PlaceInfo name;
	
	@XmlElement(name="country")
	private PlaceInfo country;
	
	@XmlElement(name="admin1")
	private PlaceInfo admin1;
	
	@XmlElement(name="admin2")
	private PlaceInfo admin2;
	
	@XmlElement(name="admin3")
	private PlaceInfo admin3;
	
	@XmlElement(name="locality1")
	private PlaceInfo locality1;
	
	@XmlElement(name="locality2")
	private PlaceInfo locality2;
	
	@XmlElement(name="postal")
	private PlaceInfo postal;
	
	@XmlElement(name="centroid")
	private PlaceGeography centroid;
	
	@XmlElement(name="boundingBox")
	private BoundingBox boundingBox;
	
	@XmlElement(name="areaRank")
	private int areaRank;
	
	@XmlElement(name="popRank")
	private int popRank;
	
	@XmlElement(name="timezone")
	private PlaceInfo timezone;

	public String getWoeid() {
		return woeid;
	}

	public void setWoeid(String woeid) {
		this.woeid = woeid;
	}

	public PlaceInfo getPlaceTypeName() {
		return placeTypeName;
	}

	public void setPlaceTypeName(PlaceInfo placeTypeName) {
		this.placeTypeName = placeTypeName;
	}

	public PlaceInfo getName() {
		return name;
	}

	public void setName(PlaceInfo name) {
		this.name = name;
	}

	public PlaceInfo getCountry() {
		return country;
	}

	public void setCountry(PlaceInfo country) {
		this.country = country;
	}

	public PlaceInfo getAdmin1() {
		return admin1;
	}

	public void setAdmin1(PlaceInfo admin1) {
		this.admin1 = admin1;
	}

	public PlaceInfo getAdmin2() {
		return admin2;
	}

	public void setAdmin2(PlaceInfo admin2) {
		this.admin2 = admin2;
	}

	public PlaceInfo getAdmin3() {
		return admin3;
	}

	public void setAdmin3(PlaceInfo admin3) {
		this.admin3 = admin3;
	}

	public PlaceInfo getLocality1() {
		return locality1;
	}

	public void setLocality1(PlaceInfo locality1) {
		this.locality1 = locality1;
	}

	public PlaceInfo getLocality2() {
		return locality2;
	}

	public void setLocality2(PlaceInfo locality2) {
		this.locality2 = locality2;
	}

	public PlaceInfo getPostal() {
		return postal;
	}

	public void setPostal(PlaceInfo postal) {
		this.postal = postal;
	}

	public PlaceGeography getCentroid() {
		return centroid;
	}

	public void setCentroid(PlaceGeography centroid) {
		this.centroid = centroid;
	}

	public BoundingBox getBoundingBox() {
		return boundingBox;
	}

	public void setBoundingBox(BoundingBox boundingBox) {
		this.boundingBox = boundingBox;
	}

	public int getAreaRank() {
		return areaRank;
	}

	public void setAreaRank(int areaRank) {
		this.areaRank = areaRank;
	}

	public int getPopRank() {
		return popRank;
	}

	public void setPopRank(int popRank) {
		this.popRank = popRank;
	}

	public PlaceInfo getTimezone() {
		return timezone;
	}

	public void setTimezone(PlaceInfo timezone) {
		this.timezone = timezone;
	}
	
}
